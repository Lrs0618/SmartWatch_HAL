void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin){

	if(GPIO_Pin == encoder_A_Pin){

		if(HAL_GPIO_ReadPin(encoder_B_GPIO_Port, encoder_B_Pin)){ //旋钮 顺时针旋转时触发

			switch(ui_page){

			case UI_Page_Home:

				break;

			case UI_Page_Menu:

				break;

			default:
				break;

			}

	    }

	}

	if(GPIO_Pin == encoder_B_Pin){

		if(HAL_GPIO_ReadPin(encoder_A_GPIO_Port, encoder_A_Pin)){ //旋钮 逆时针旋转时触发

			switch(ui_page){

			case UI_Page_Home:

				break;

			case UI_Page_Menu:

				break;

			default:
				break;

			}

	    }

	}

	if(GPIO_Pin == button_A_Pin){ //按钮A 按下时触发

		switch(ui_page){

		case UI_Page_Home:

			break;

		case UI_Page_Menu:

			break;

		default:
			break;

		}
	}

	if(GPIO_Pin == button_B_Pin){ //按钮B 按下时触发

		switch(ui_page){

		case UI_Page_Home:

			break;

		case UI_Page_Menu:

			break;

		default:
			break;

		}

	}

}

